
//예제. do while 반복문을 이용하여 1부터 ~ 5까지 반복해서
//     1  
//     2  
//     3
//     4
//     5  
//출력하기 
public class While03 {
	public static void main(String[] args) {
		//초기식
		int i=1;
		
		do {
			System.out.println(i);//반복할문장
			
			i++;//증감식
			
		} while (i<=5);//조건식
		
		
	
		
	}

}






