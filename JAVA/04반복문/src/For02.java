
//예제. for문을 제어 하는 제어변수 값 반복해서 출력
public class For02 {

	public static void main(String[] args) {
		//for문을 제어할 제어변수 i선언
		int i;
		
		//i변수의 초기값을 1로 저장하여
		//1씩 증가하면서 4가될때까지 반복 수행합니다
		for(i=1;  i<=4;  i++) {
			//반복실행할코드;
			System.out.println(i);
			
		}
		//for반복문을 벗어난 후에 i제어변수 값은 조건식에 적어 준값보다 1큽니다
		System.out.println("----->>" + i);
		
		
	}

}






